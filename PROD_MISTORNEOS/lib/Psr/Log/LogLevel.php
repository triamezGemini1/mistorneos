<?php

namespace Psr\Log;

/**
 * Clase LogLevel - Constantes para niveles de log (PSR-3)
 * Implementación simple para cuando el paquete psr/log no está instalado
 */
class LogLevel
{
    const EMERGENCY = 'emergency';
    const ALERT = 'alert';
    const CRITICAL = 'critical';
    const ERROR = 'error';
    const WARNING = 'warning';
    const NOTICE = 'notice';
    const INFO = 'info';
    const DEBUG = 'debug';
}












